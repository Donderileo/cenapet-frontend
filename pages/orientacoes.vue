        <b> SIGPET </b>
        <br><br>
        A nova plataforma do Sistema de Gestão do Programa de Educação Tutorial (SIGPET 2.0), desenvolvida pelo Ministério da Educação em nova versão, tem como objetivo otimizar as ações do Programa de Educação Tutorial. Os módulos de Pagamento de Bolsa, Planejamento Anual, Relatório de Atividades, Custeio e Prestação de Contas foram desenvolvidos para tornar mais amigável e intuitiva a sua navegação. A criação do sistema considerou a legislação vigente e adequou todas as suas ações, de modo que, a sua execução cumpra exatamente o que está determinado. A facilidade e transparência fazem do SIGPET 2.0 a ferramenta ideal para a gestão do PET e, a partir de agora, todos os usuários com perfis pré-definidos poderão acompanhar o trabalho desenvolvido pelo seu Grupo PET, assim como, os assuntos mais específicos de cada perfil, como por exemplo, a utilização da verba de custeio.
