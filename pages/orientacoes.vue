        <b> Legislação </b>
        <br>
        <br>
        O Programa de Educação Tutorial foi oficialmente instituído pela Lei 11.180/2005 e regulamentado pelas Portarias nº 3.385/2005, nº 1.632/2006 e nº 1.046/2007. A regulamentação do PET define como o programa deve funcionar, qual a constituição administrativa e acadêmica, além de estabelecer as normas e a periodicidade do processo de avaliação nacional dos grupos.
        <br>
        <br>
        A Portaria 976/2010 trouxe inovações para a estrutura do PET como, por exemplo, a flexibilização e dinamização da estrutura dos grupos, a união do PET com o Conexões de Saberes, a definição de tempo máximo de exercício da tutoria, a aproximação com a estrutura acadêmica da universidade e a definição de estruturas internas de gestão do PET. (Informações site Ministério da Educação)
